/**
  * op_sub - op add
  * @a: number
  * @b: number
  * Return: soult operation
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
* op_add - sum of two variables
*@b: number of add
*@a: number of add
*
* Return: ...
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
* op_div - Divide two numbers
* @a: The first number
* @b: The second number
*
* Return: The result
*/

int op_div(int a, int b)
{
	return (a / b);
}
/**
* op_mul - product for two variables
* @a: var
* @b: var
*
* Return: ...
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
* op_mod - the division of two numbers
* @a: The first number
* @b: The second number
*
* Return: result
*/
int op_mod(int a, int b)
{
	return (a % b);
}
